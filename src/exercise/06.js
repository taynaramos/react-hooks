import * as React from 'react'
// 🐨 you'll want the following additional things from '../pokemon':
// fetchPokemon: the function we call to get the pokemon info
// PokemonInfoFallback: the thing we show while we're loading the pokemon info
// PokemonDataView: the stuff we use to display the pokemon info
import {
  fetchPokemon,
  PokemonDataView,
  PokemonForm,
  PokemonInfoFallback,
} from '../pokemon'

class ErrorBoundary extends React.Component {
  constructor(props) {
    super(props)
    this.state = {hasError: false}
  }

  static getDerivedStateFromError(error) {
    // Update state so the next render will show the fallback UI.
    return {hasError: true}
  }

  render() {
    if (this.state.hasError) {
      // You can render any custom fallback UI
      return this.props.fallback
    }

    return this.props.children
  }
}

const REQ_STATUS = {
  IDLE: 'idle',
  PENDING: 'pending',
  RESOLVED: 'resolved',
  REJECTED: 'rejected',
}

function PokemonInfo({pokemonName}) {
  const [state, setState] = React.useState({
    status: REQ_STATUS.IDLE,
    pokemon: null,
    error: null,
  })

  React.useEffect(() => {
    if (pokemonName) {
      setState(s => ({...s, status: REQ_STATUS.PENDING}))
      fetchPokemon(pokemonName)
        .then(res => {
          setState({status: REQ_STATUS.RESOLVED, pokemon: res})
        })
        .catch(err => {
          setState(s => ({...s, status: REQ_STATUS.REJECTED, error: err}))
        })
    }
  }, [pokemonName])

  switch (state.status) {
    case REQ_STATUS.IDLE:
      return <div>Submit a pokemon</div>

    case REQ_STATUS.PENDING:
      return <PokemonInfoFallback name={pokemonName} />

    case REQ_STATUS.RESOLVED:
      return <PokemonDataView pokemon={state.pokemon} />

    case REQ_STATUS.REJECTED:
      throw Error('Testing error boundary component')

    default:
      throw Error('that should be impossible')
  }

  // 🐨 Have state for the pokemon (null)
  // 🐨 use React.useEffect where the callback should be called whenever the
  // pokemon name changes.
  // 💰 DON'T FORGET THE DEPENDENCIES ARRAY!
  // 💰 if the pokemonName is falsy (an empty string) then don't bother making the request (exit early).
  // 🐨 before calling `fetchPokemon`, clear the current pokemon state by setting it to null.
  // (This is to enable the loading state when switching between different pokemon.)
  // 💰 Use the `fetchPokemon` function to fetch a pokemon by its name:
  //   fetchPokemon('Pikachu').then(
  //     pokemonData => {/* update all the state here */},
  //   )
  // 🐨 return the following things based on the `pokemon` state and `pokemonName` prop:
  //   1. no pokemonName: 'Submit a pokemon'
  //   2. pokemonName but no pokemon: <PokemonInfoFallback name={pokemonName} />
  //   3. pokemon: <PokemonDataView pokemon={pokemon} />

  // 💣 remove this
  // return 'TODO'
}

function App() {
  const [pokemonName, setPokemonName] = React.useState('')

  function handleSubmit(newPokemonName) {
    setPokemonName(newPokemonName)
  }

  return (
    <ErrorBoundary fallback={<div>ERRO PEGO PELO ERROR BOUNDARY</div>}>
      <div className="pokemon-info-app">
        <PokemonForm pokemonName={pokemonName} onSubmit={handleSubmit} />
        <hr />
        <div className="pokemon-info">
          <PokemonInfo pokemonName={pokemonName} />
        </div>
      </div>
    </ErrorBoundary>
  )
}

export default App
